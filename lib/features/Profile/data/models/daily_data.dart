class DailyData {
  double waterConsumed;
  int steps;
  double caloriesBurned;
  double sleepHours;
  int heartRate;
  DateTime date;

  DailyData({
    required this.waterConsumed,
    required this.steps,
    required this.caloriesBurned,
    required this.sleepHours,
    required this.heartRate,
    required this.date,
  });
}